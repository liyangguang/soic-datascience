<h3 id="gre" class="content-sub">GRE Scores</h3>
<p class="content-content">All applicants applying to the Data Science program must submit official GRE scores, and those official scores must be sent directly to Indiana University. Our School code is 1324. GRE scores are good for five years from the date of the exam. If you have an ETS score sheet, you can scan it and send it as an attachment to datasci@indiana.edu. We can consider those scores as unofficial scores until the official scores arrive.
<br><br>
For the Data Science program, we take a very holistic approach to reviewing applications. As such we do not have specific minimum admissions requirements that students must meet to qualify for the program. While we do not have minimum requirements for the GRE, we do generally look for scores around Q-160, V-153, and AW-4.0.
</p>
<h3 id="toefl" class="content-sub">TOEFL or IELTS Scores (for international students)</h3>
<p class="content-content">The TOEFL and IELTS are measures of English proficiency. International applicants are required to submit official TOEFL or IELTS scores. You will need to have your official TOEFL or IELTS scores sent directly to Indiana University. Our School code is 1324. We expect a minimum of 100 on the TOEFL or a minimum of 7.0 on the IELTS. TOEFL and IELTS scores are good for two years from the date of the exam. If you have an ETS score sheet, scan it and send it as an attachment to datasci@indiana.edu. We can use those scores as unofficial scores until the official scores arrive.
<br><br>
While we do look for a minimum TOEFL score of 100, a lesser score will not automatically disqualify you from admission. Each aspect of your profile will be evaluated by the Admissions Committee before a final decision is rendered.</p>

